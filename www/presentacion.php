                                    <span class="date">Diciembre, 2018</span>
                                    <h2>“Lista Viernes”<br/>
                                        o Lista Robinson Electoral</h2>
                                    <h3>Whitepaper</h3>
                                </header>


                                <h4>Contexto: nuevo régimen electoral</h4>
                                <p>El pasado 7 de diciembre de 2018 entró en vigor el nuevo art 58 bis de la Ley del Régimen Electoral que regula explícitamente el tratamiento de datos ideológicos de los ciudadanos, la captación de sus datos personales hechos públicos en internet y redes sociales y el envió de propaganda electoral por medios electrónicos.</p>
                                <p>Este novedoso régimen no ha venido acompañado de medidas que permitan al ciudadano evitar estos tratamientos de forma preventiva.</p>
                                <p>Sólo se obliga a los partidos políticos a habilitar medios de oposición a posteriori frente al tratamiento, una vez sus datos ya han sido captados y tratados por los partidos políticos.</p>
                                <h4>“Lista Viernes”: concepto y denominación</h4>
                                <p>Proponemos la Lista Viernes por analogía a la “Lista Robinson” (en la que cualquiera puede inscribirse y evitar que las empresas de marketing directo le remitan comunicaciones comerciales por vía electrónica a los números de teléfono y direcciones de correo señaladas).</p>
                                <p>“Robinson” viene del libro “Robinson Crusoe” que naufragó en una isla desierta a la que no llegaba la publicidad.</p>
                                <p>Robinson llamó “Viernes” a un indígena que rescató y que se convirtió en su compañero de aventura.</p>
                                <p>La Lista Viernes tendrá la misma función que la lista Robinson, pero en relación con los partidos políticos.</p>
                                <p>Ambas listas persiguen el mismo fin: proteger la privacidad de los ciudadanos.</p>
                                <h4>Funcionamiento</h4>
                                <p>Todos los ciudadanos con derecho de voto podrán inscribirse gratuitamente para:</p>
                                <ul>
                                    <li>
                                        Hacer valer su oposición a la recepción de propaganda electoral en las direcciones de correo electrónico y teléfonos de contacto que se suministren.<br/>
                                        Sólo será necesario inscribirse una sola vez, para oponerse a la propaganda de todos los partidos que concurran a cada circunscripción electoral.
                                    </li>
                                    <li>
                                        Hacer valer su oposición al tratamiento por parte de partidos políticos de los datos de ideología (o cualesquiera otros) “hechos manifiestamente públicos” en páginas web y redes sociales, con fines electorales.
                                    </li>
                                </ul>
                                <p>La lista no será pública, no servirá a ninguna otra finalidad ni se cederá a terceros. Únicamente a las autoridades públicas si la solicitan y en los casos legalmente previstos.</p>
                                <p>Los partidos políticos podrán depurar sus propias bases de datos mediante un sistema que les permitirá saber a cuáles de sus contactos no deben enviar publicidad, sin entregar su base de datos, ni acceder en ningún momento a la Lista Viernes.</p>
                                <p>El objetivo es que su uso también sea gratuito para los partidos políticos, para maximizar su alcance y eficacia.</p>
                                <h4>Una herramienta de mitigación preventiva del riesgo</h4>
                                <p>La herramienta que proponemos no es ni novedosa ni innovadora. Pero creemos que es conveniente, y que bien podría haberse creado junto con la nueva normativa.</p>
                                <p>Parece conveniente que la sociedad se dote de cualquier herramienta de protección de la privacidad, especialmente en un momento de máxima preocupación ante campañas de manipulación y desinformación ante procesos electorales.</p>
                                <p>Con independencia de lo anterior, no parece coherente que una persona esté más protegida como consumidor frente ante el “spam comercial”, que como ciudadano frente al “spam político”.</p>
                                <h4>Promotores y financiación</h4>
                                <p>La iniciativa es de Secuoya Group, un grupo de especialistas en protección de datos, que ha ideado, impulsado y financiado la puesta en marcha de esta iniciativa.</p>
                                <p>Secuoya Group no tiene ni quiere tener, adscripción a ideología política alguna.</p>
                                <p>Para mantener la independencia del proyecto, y también para ayudar a la difusión pública del mismo, queremos promover una campaña de crowdfunding.</p>
                                <p>El crowdfunding debe servir para financiar:</p>
                                <ul>
                                    <li>Un buen ancho de banda para que no se “caiga” el servidor en caso de afluencia masiva a la web.</li>
                                    <li>Seguridad informática a la altura de la importancia del proyecto.</li>
                                    <li>Desarrollar una aplicación avanzada propia para permitir ese cruce “sin contacto” entre nuestra lista y las bases de datos de los partidos políticos. Para mayor seguridad y control, no querríamos contratar a terceros para ejecutarla.</li>
                                    <li>Nuevos desarrollos que tenemos pensados para proteger más la privacidad de los inscritos.</li>
                                </ul>
                                <h4>Aportación del proyecto a una Fundación</h4>
                                <p>Para hacer aún más patente la independencia y ausencia de ánimo de lucro de esta iniciativa, la titularidad sobre los dominios de las webs, los derechos de uso sobre la base de datos, y la propia base de datos se transmitirá a una fundación que se constituirá al final de la campaña de crowdfunding.</p>
                                <p>La fundación gestionará de forma independiente la herramienta. Su continuidad se financiará con donaciones individuales y de organizaciones que, (esperemos), consideren esta iniciativa digna de sus políticas de responsabilidad social corporativa.</p>
                                <p>Las cantidades donadas a esta Fundación proporcionan las mismas deducciones fiscales que las realizadas a a cualquier otra ONG.</p>
                                <h4>Un  proyecto basado en código abierto</h4>
                                <p>El proyecto se desarrollará en código abierto, y se publicará para que cualquiera pueda auditarlo y reutilizarlo, por ejemplo, en otro país de la UE para los mismos fines.</p>
                                <h4>Política –abreviada- de privacidad de la Lista Viernes</h4>
                                <p>Los datos personales incorporados a la Lista Viernes sólo servirán para las finalidades explicadas en este documento. No serán cedidos a terceros distintos de la Fundación, o de las autoridades públicas, caso de que la soliciten en los supuestos previstos en la Ley.</p>
                                <p>Tus datos serán tratados hasta que nos comuniques tu voluntad de que dejemos de hacerlo.</p>
                                <p>Si no conseguimos un número relevante de inscritos, destruiremos de forma segura la base de datos, después de las elecciones de mayo de 2019.</p>

